import React from 'react'
import Title from '@/components/title'

import styles from '@/styles/bylaws.module.css'

export default function Bylaws() {
  return <>
    <Title>Bylaws</Title>

    <div className={styles.bylaws}>
      <h1>Renaissance Neighborhood Association Bylaws</h1>

      <p><i>Amended bylaws of Renaissance Neighborhood Association (RNA) as of April 18, 2023</i></p>

      <h2>ARTICLE I. NAME AND PURPOSE</h2>

      <p>The name of the association shall be the “Renaissance Neighborhood Association.”</p>

      <p>The association will operate exclusively in an effort to maintain, protect, enhance, and improve the quality of life of the neighborhood within the boundaries set forth in these bylaws.</p>

      <h2>ARTICLE II. BOUNDARIES</h2>

      <p>For the purposes of this association, the Renaissance Neighborhood is defined as the area bounded by Lewis Avenue on the west, 15th Street on the south, Harvard Avenue on the east, and 11th Street on the north, Tulsa, Oklahoma.</p>

      <h2>ARTICLE III. MEMBERSHIP</h2>

      <h3>Section 1. Membership</h3>

      <p>There shall be one class of membership. This membership shall be allowed one vote per each paid adult membership, as detailed in the “Dues section”. Membership is open to any resident, nonresident property owners, business, school, or church located within the designated boundaries. Members in good standing shall have paid annual dues as set forth in the bylaws.</p>

      <h3>Section 2. Dues</h3>

      <p>Dues to be a member in good standing shall be $15 per individual / $20 per household (two adult votes per household) / $30 per business membership (one vote per membership.) Dues are for one year beginning January 1st and ending December 31st. This dues structure is effective the January 1st after the amended date listed above.</p>

      <h3>Section 3. Meetings</h3>

      <p>Meetings of the association shall be held at least two times a year, once in the fall and once in the spring, in the evening, or at such other date and time as shall be designated by the Board and stated in a notice of the meeting. The board President shall either chair membership meetings or delegate another board person to do so.</p>

      <h3>Section 4. Special Meetings</h3>

      <p>Special meetings may be called by the President, by not less than two (2) officers, or by a group of not less than twenty-five (25) association members in good standing. When a special meeting is called by association members, Board Officers should be notified by the requesting association members. The board President shall either chair the special meeting or delegate another board person to do so.</p>

      <h3>Section 5. Notice of Meetings</h3>

      <p>Notice of all meetings stating the place, day, and hour shall be posted throughout the neighborhood and distributed by social media or any other means available. Postcards may also be mailed to members providing funds are available. This notice shall be given not less than three (3) days prior to the meeting.</p>

      <h3>Section 6. Voting</h3>

      <p>At all meetings of members, each member in good standing shall be entitled to one vote. Accommodations will be made for members unable to attend meetings and the process will be shared via e-mail prior to the meeting. The voting process can be changed under extraordinary circumstances by a vote of the Board of Directors.</p>

      <h2>ARTICLE IV. BOARD OF DIRECTORS</h2>

      <h3>Section 1. Composition</h3>

      <p>The board of directors shall include the President, Vice President, Secretary, Treasurer, and up to five (5) Members at Large who will fill other duties as deemed necessary. Directors shall be of adult age and in good standing of the Association.</p>

      <h3>Section 2. Election</h3>

      <p>Board Officers (President, Vice President, Secretary, Treasurer) shall be elected by the Association members (in good standing) at the last regular meeting of the year. If the election is not held at the last meeting of the year, the election of Officers shall be held as soon thereafter as convenient. Not later than one (1) month prior to the fall meeting, the President shall create and appoint, with the approval of the Board Officers, a nominating committee. The focus of the nominating committee is to propose a slate of Officers. Additional nominations may be received from membership (in good standing) at any time up until the pre-announced date. All nominees must be members in good standing with the Association.</p>

      <p>Members at large shall be nominated by current Board members and voted onto the Board with a majority vote of active board members at the time of the vote.</p>

      <h3>Section 3. Terms</h3>

      <p>Terms of the Officers shall commence on January 1st, after the vote of the membership. Each Officer and Member at Large shall serve a term of 2-years with no limit on the number of terms. </p>

      <h3>Section 4. Removal</h3>

      <p>Any Board Officer elected by the membership may be removed by a petition signed by no less than sixty percent (60%) of the members and then a majority vote of the members at the next scheduled vote or a super (3/4) majority vote of the Board whenever, in their judgement, the best interests of the Association would be served thereby.</p>

      <p>Members at large can be removed by a super (3/4) majority vote of the Board, whenever, in their judgement, the best interests of the Association would be served thereby.</p>

      <p>Any member of the Board who files for public office shall be required to take a leave of absence until such time as the election of said office occurs. Election to public office shall automatically disqualify members to be eligible to be on the Board and shall constitute automatic removal from the Board.</p>

      <h3>Section 5. Vacancies</h3>

      <p>A vacancy in any Office or Member at Large position may be filled by the Board with a majority vote for the unexpired portion of the term.</p>

      <h3>Section 6. Quorum</h3>

      <p>A majority of the Board shall constitute a quorum.</p>

      <h2>ARTICLE V. OFFICERS</h2>

      <h3>Section 1. Composition </h3>
      <p>The officers of the association shall be a President, Vice President, Secretary, and Treasurer. </p>

      <h3>Section 2. Duties </h3>

      <p><b>President</b>: The President shall carry into effect all directions and resolutions of the Board. The President shall preside at all meetings of the general membership and the Board. The President shall, unless the Board otherwise provides, be an ex-officio member of all committees and shall have such other or further duties and authority as may be prescribed by the Board. </p>

      <p><b>Vice President</b>: In absence of the President or in the event of their inability or refusal to act, the Vice President shall perform the duties of the President, and when so acting, shall have all the powers of and be subject to all the restrictions upon the President. The Vice President shall perform such other duties as may be assigned to them by the President or by the Board. </p>

      <p><b>Secretary</b>: The Secretary shall keep the minutes of the meetings of the members and the Board, be custodian of the association records, and in general perform such other duties as may be assigned by the Board. The Secretary shall keep a record giving the names and addresses of the Board. </p>

      <p><b>Treasurer</b>: The Treasurer shall have charge and custody of and be responsible for all funds and securities of the association, receive and give receipts for monies due and payable to the association from any source whatsoever, and deposit all such monies in the name of the association in such banks, trust companies, or other depositories as shall be selected by the Board and other duties as may be assigned by the Board. The Treasurer shall keep correct and complete records of all association finances to include reporting at general meetings and Board meetings. </p>

      <h2>ARTICLE VI. CONTRACTS, CHECKS, DEPOSITS AND FUNDS </h2>

      <h3>Section 1. Contracts </h3>

      <p>The Board may authorize any officer or officers agent, or agents of the association, in addition to the officers so authorized by these bylaws, to enter into any contract or execute and deliver any instrument in the name of the association, and such authority may be general or confined to specific instances. </p>

      <h3>Section 2. Checks, Drafts, etc. </h3>

      <p>All checks, drafts, and other orders for the payment of money, notes, or other evidence of indebtedness issued in the name of the association, shall require the signature of the President and Treasurer in order to authorize any disbursements from the association funds. The RNA debit card shall be used only by the President and the Treasurer. </p>

      <h3>Section 3. Deposits </h3>

      <p>All funds of the association shall be deposited to the credit of the association in such banks, trust companies or other depositories as the Board may select. Names on the bank account shall reflect the current RNA President and Treasurer. </p>

      <h3>Section 4. Gifts </h3>

      <p>The Board may accept, on behalf of the association, any contribution, gift or bequest for the general purpose or for any special purpose of the association. </p>

      <h2>ARTICLE VII. BOOKS AND RECORDS </h2>

      <p>All books and records shall be subject to audit at the direction of the Board. </p>

      <h2>ARTICLE VIII. AMENDMENT TO BYLAWS </h2>

      <p>These bylaws may be altered, amended, or repealed and new bylaws may be adopted by the membership at any regular or special meetings of the membership with a majority vote, as long as notice of such changes is made available to the membership one month in advance of the meeting.</p>

      <p className="; text-center border-t border-black py-4"><a href="/files/rna-bylaws-20230418.pdf" target="_blank">Download these bylaws as a PDF</a></p>
    </div>
  </>
}

